    # Check for greetings first
    for greeting_key, greeting_response in OROMO_GREETINGS.items():
        if greeting_key in user_input_lower:
            return greeting_response
    
    # Check for specific responses
    for keyword, response in OROMO_RESPONSES.items():
        if keyword in user_input_lower:
            return response
    
